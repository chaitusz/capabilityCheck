package capabilityCheck;

public class SwitchCheck {

	public static void main(String[] args) {

		int i = 4;

		switch (i) {

		case 0:

			System.out.println("Value of i is Zero");
			break;
		case 1:
			System.out.println("Value of i is one");
			break;

		case 2:
			System.out.println("Value of i is Two");
			break;

		case 3:
			System.out.println("Value of i is Three");
			break;
			
		case 4:
			System.out.println("Value of is is Four");
			break; 

		default:
			System.out.println("Value of i is not among 1,2,3,4");

			break;

		}
	}

}
